    public function search(Request $request)
    {
        $data= $request->all();

        $users= DB::table('users')
            ->select(
                'id', 
                'name', 
                'email', 
                'email_verified_at', 
                'remember_token', 
                'created_at',
                'updated_at')
            ->where('name', 'like', $data['word'].'%')->get();

        return json_encode($users);
    }
